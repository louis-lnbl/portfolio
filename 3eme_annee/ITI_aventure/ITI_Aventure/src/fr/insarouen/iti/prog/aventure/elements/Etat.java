package fr.insarouen.iti.prog.aventure.elements;

/**
 * L'énumération Etat représente les différents états possibles d'un objet dans le jeu.
 * Ces états peuvent être utilisés pour décrire l'état d'un coffre, d'une porte ou de tout autre
 * objet susceptible de changer d'état au cours du jeu.
 * 
 */
public enum Etat {
    FERME, 
    OUVERT, 
    CASSE, 
    VERROUILLE, 
    DEVERROUILLE };

